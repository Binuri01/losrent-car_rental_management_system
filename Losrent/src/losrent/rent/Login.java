    private void userLogin(String username, String pass) throws SQLException {
        Connection dbconn = DBConnection.connectDB();
        if(dbconn != null)
        {
            try
            {
                PreparedStatement st = (PreparedStatement)dbconn.prepareStatement("SELECT * FROM `app_reg` WHERE Username=? AND Password=?" );

                st.setString(1,username);
                st.setString(2, pass);
                ResultSet res = st.executeQuery();
                if(res.next())
                {
                    System.out.println("Username: " +username);
                    System.out.println("Password: " +pass);
                    
                    dispose();
                    Home d = new Home();
                    d.setVisible(true);
                }
                else
                {
                    System.out.println("Username: " +username);
                    System.out.println("Password: " +pass);
                    JOptionPane.showMessageDialog(this,"Username and Password could not found","Error",JOptionPane.ERROR_MESSAGE);
                }
            }
            catch(SQLException ex)
            {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE,null,ex);
            }
        }
        else
        {
            System.out.println("The connection is not available");
        }
    }
